#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	x;

	x = (unsigned char) c;
	if ((x >= 'a' && x <= 'z') || (x >= 'A' && x <= 'Z'))
		return (1);
	return (0);
}
